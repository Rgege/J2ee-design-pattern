package com.jdp.structuralPattern.proxy.cglib;

import net.sf.cglib.proxy.MethodInterceptor;
import net.sf.cglib.proxy.MethodProxy;

import java.lang.reflect.Method;

/**
 * CglibProxyDemoIntercepter   定制化拦截器
 *
 * @author xiong rui
 * @version 1.0.0
 * @date 2019/2/22
 **/
public class CglibProxyDemoIntercepter implements MethodInterceptor  {

    /**
     *
     * @param o 表示要进行增强的对象
     * @param method 表示拦截的方法
     * @param objects 数组表示参数列表，基本数据类型需要传入其包装类型，如int-->Integer、long-Long、double-->Double
     * @param methodProxy 表示对方法的代理，invokeSuper方法表示对被代理对象方法的调用
     * @return Object
     * @throws Throwable
     */
    @Override
    public Object intercept(Object o, Method method, Object[] objects, MethodProxy methodProxy) throws Throwable {
        System.out.println("=========================cglib do somthing before");
        methodProxy.invokeSuper(o,objects);
        System.out.println("=========================cglib do somthing after");

        return null;
    }
}
